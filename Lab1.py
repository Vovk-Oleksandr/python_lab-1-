# --- Завдання 1 ---
# Створіть чотири змінні. Введення цілих і дробових чисел.
a = int(input("Введіть перше ціле число: "))
b = int(input("Введіть друге ціле число: "))
c = float(input("Введіть перше дробове число: "))
d = float(input("Введіть друге дробове число: "))

# --- Завдання 2 ---
# Виконаємо основні математичні операції над числами a і b.
results = [
    a + b,    # додавання
    a - b,    # віднімання
    a * b,    # множення
    a / b if b != 0 else "ділення на нуль",  # ділення
    a ** b,   # піднесення до ступеня
    a // b if b != 0 else "ділення на нуль", # цілочисельне ділення
    a % b if b != 0 else "ділення на нуль"   # остача від ділення
]

print("Список результатів:", results)

# --- Завдання 3 ---
# Визначимо кількість елементів та виведемо парні
print("Кількість елементів у списку:", len(results))

even_elements = [x for x in results if isinstance(x, int) and x % 2 == 0]
print("Парні елементи списку:", even_elements)

# --- Завдання 4 ---
# Поміняємо місцями другий і п’ятий елементи списку (якщо можливо)
if len(results) >= 5:
    results[1], results[4] = results[4], results[1]
    print("Список після обміну 2 і 5 елементів:", results)
else:
    print("У списку недостатньо елементів для обміну.")

